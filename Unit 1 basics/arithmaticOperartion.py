# Arithmetic operations
x = 10
y = 5

# Addition
sum_result = x + y
print("Sum:", sum_result)  # Output: 15

# Subtraction
difference = x - y
print("Difference:", difference)  # Output: 5

# Multiplication
product = x * y
print("Product:", product)  # Output: 50

# Division
quotient = x / y
print("Quotient:", quotient)  # Output: 2.0
