import math

x = 16
y = 3

sqrt_x = math.sqrt(x)
power = math.pow(x, y)
factorial_y = math.factorial(y)
cos_x = math.cos(math.radians(60))
log_x = math.log(x, 2)

print("Square root of x:", sqrt_x)
print("x raised to the power y:", power)
print("Factorial of y:", factorial_y)
print("Cosine of 60 degrees:", cos_x)
print("Log base 2 of x:", log_x)